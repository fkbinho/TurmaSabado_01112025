package au_29112025;

public class OperadorAtribuicao {

	public static void main(String[] args) {
		
		int valor = 10;
		System.out.println(valor);
		
		valor += 5; // valor = valor + 5;
		System.out.println(valor);
		
		valor -= 3; // valor = valor - 3;
		System.out.println(valor);
		
		valor *= 2; // valor = valor * 2;
		System.out.println(valor);
		
		valor /= 3; // valor = valor / 3;
		System.out.println(valor);
		
		valor %= 3; // valor = valor % 3;
		System.out.println(valor);
	}
}
